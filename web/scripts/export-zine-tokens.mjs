#!/usr/bin/env node
/**
 * export-zine-tokens.mjs — the design-parity backbone for the web client.
 *
 * Reads the Flutter design tokens at ../app/lib/core/ui/zine.dart and emits:
 *   - src/styles/tokens.css      (:root { --zine-*: ... })
 *   - tailwind.zine.cjs          (CommonJS fragment exporting `theme.extend`)
 *
 * NOTHING is hand-copied: every color / radius / border width / duration is
 * parsed from zine.dart so the web stays in lockstep with the app. Re-runs on
 * `predev` and `prebuild` (see package.json). If zine.dart changes, the web
 * picks it up on the next build — no manual edits.
 *
 * Flutter color literals are 0xAARRGGBB. Conversion to CSS:
 *   - AA == FF  -> #RRGGBB
 *   - otherwise -> rgba(r, g, b, a/255)
 *
 * Shadows are intentionally NOT parsed from Dart's BoxShadow list syntax;
 * the zine language defines them as HARD offsets (never blurred), so we emit
 * them canonically here, referencing the parsed ink / blueInk / coral vars.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = resolve(__dirname, '..');
const ZINE_DART = resolve(WEB_ROOT, '../app/lib/core/ui/zine.dart');
const OUT_CSS = resolve(WEB_ROOT, 'src/styles/tokens.css');
const OUT_TW = resolve(WEB_ROOT, 'tailwind.zine.cjs');

function fail(msg) {
  console.error(`\n[export-zine-tokens] ERROR: ${msg}\n`);
  process.exit(1);
}

let src;
try {
  src = readFileSync(ZINE_DART, 'utf8');
} catch (e) {
  fail(
    `could not read ${ZINE_DART}.\n` +
      `The web client must live inside the monorepo next to app/ so this ` +
      `relative path resolves. (${e.message})`,
  );
}

/** 0xAARRGGBB -> css color string */
function toCss(hex8) {
  const a = parseInt(hex8.slice(0, 2), 16);
  const r = parseInt(hex8.slice(2, 4), 16);
  const g = parseInt(hex8.slice(4, 6), 16);
  const b = parseInt(hex8.slice(6, 8), 16);
  if (a === 255) {
    return `#${hex8.slice(2).toUpperCase()}`;
  }
  const alpha = +(a / 255).toFixed(3);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

// ---- parse colors: `static const <name> = Color(0xAARRGGBB);` ----
const colors = {};
const colorRe = /static const (\w+)\s*=\s*Color\(0x([0-9A-Fa-f]{8})\)/g;
for (let m; (m = colorRe.exec(src)); ) {
  colors[m[1]] = toCss(m[2]);
}

// ---- parse numeric doubles: `static const double <name> = <num>;` ----
const doubles = {};
const dblRe = /static const double (\w+)\s*=\s*([\d.]+)/g;
for (let m; (m = dblRe.exec(src)); ) {
  doubles[m[1]] = parseFloat(m[2]);
}

// ---- parse durations: `static const <name> = Duration(milliseconds: N);` ----
const durations = {};
const durRe = /static const (\w+)\s*=\s*Duration\(milliseconds:\s*(\d+)\)/g;
for (let m; (m = durRe.exec(src)); ) {
  durations[m[1]] = parseInt(m[2], 10);
}

if (Object.keys(colors).length === 0) fail('parsed 0 colors — the parser drifted from zine.dart syntax.');

// ---- canonical hard-offset shadows (never blurred) ----
const shadows = {
  shadow: '6px 7px 0 var(--zine-ink)',
  shadowSm: '3px 3px 0 var(--zine-ink)',
  shadowXs: '2px 2px 0 var(--zine-ink)',
  shadowPressed: '1px 1px 0 var(--zine-ink)',
  shadowFocus: '5px 6px 0 var(--zine-blueInk)',
  shadowError: '5px 6px 0 var(--zine-coral)',
};

// ---- verification spot-checks (Phase 0 acceptance gate) ----
const EXPECT = {
  paper: '#F9F7ED',
  lime: '#BFEB56',
  coral: '#FE674C',
  ink: '#231B14',
  blueInk: '#007D7F',
};
const mismatches = [];
for (const [k, v] of Object.entries(EXPECT)) {
  if ((colors[k] || '').toUpperCase() !== v.toUpperCase()) {
    mismatches.push(`${k}: expected ${v}, got ${colors[k] ?? '(missing)'}`);
  }
}
if (mismatches.length) {
  fail('token spot-check failed — fix the parser before continuing:\n  ' + mismatches.join('\n  '));
}

// ---- emit tokens.css ----
const cssLines = [
  '/* GENERATED by scripts/export-zine-tokens.mjs — DO NOT EDIT BY HAND. */',
  '/* Source of truth: ../app/lib/core/ui/zine.dart. Re-run via `npm run prebuild`. */',
  ':root {',
  '  /* surfaces + ink + poster accents + derived tints */',
];
for (const [name, val] of Object.entries(colors)) {
  cssLines.push(`  --zine-${name}: ${val};`);
}
cssLines.push('', '  /* geometry */');
for (const [name, val] of Object.entries(doubles)) {
  cssLines.push(`  --zine-${name}: ${val}px;`);
}
cssLines.push('', '  /* motion */');
for (const [name, val] of Object.entries(durations)) {
  cssLines.push(`  --zine-${name}: ${val}ms;`);
}
cssLines.push('', '  /* hard offset shadows (never blurred) */');
for (const [name, val] of Object.entries(shadows)) {
  cssLines.push(`  --zine-${name}: ${val};`);
}
cssLines.push('}', '');
mkdirSync(dirname(OUT_CSS), { recursive: true });
writeFileSync(OUT_CSS, cssLines.join('\n'));

// ---- emit tailwind.zine.cjs (theme.extend fragment) ----
const twColors = {};
for (const name of Object.keys(colors)) twColors[name] = `var(--zine-${name})`;

// radius / border-width tailwind keys derived from the parsed doubles
const radiusMap = { r: 'zine', rSm: 'zineSm', rField: 'zineField', rBadge: 'zineBadge' };
const borderMap = { bw: 'zine', bwLg: 'zineLg' };
const borderRadius = {};
const borderWidth = {};
for (const [dart, tw] of Object.entries(radiusMap)) {
  if (doubles[dart] != null) borderRadius[tw] = `${doubles[dart]}px`;
}
for (const [dart, tw] of Object.entries(borderMap)) {
  if (doubles[dart] != null) borderWidth[tw] = `${doubles[dart]}px`;
}

const boxShadow = {};
for (const name of Object.keys(shadows)) {
  // tailwind key: shadow -> 'zine', shadowSm -> 'zine-sm', etc.
  const key = name === 'shadow' ? 'zine' : 'zine-' + name.replace(/^shadow/, '').replace(/^([A-Z])/, (s) => s.toLowerCase());
  boxShadow[key] = `var(--zine-${name})`;
}

const transitionDuration = {};
for (const [name, val] of Object.entries(durations)) {
  const key = name === 'dur' ? 'zine' : 'zine-' + name.replace(/^dur/, '').toLowerCase();
  transitionDuration[key] = `${val}ms`;
}

const themeExtend = {
  colors: twColors,
  borderRadius,
  borderWidth,
  boxShadow,
  fontFamily: {
    display: ['Fredoka', 'system-ui', 'sans-serif'],
    body: ['Nunito', 'system-ui', 'sans-serif'],
    mono: ['"Space Mono"', 'ui-monospace', 'monospace'],
  },
  transitionDuration,
};

const twOut =
  '// GENERATED by scripts/export-zine-tokens.mjs — DO NOT EDIT BY HAND.\n' +
  '// Imported by tailwind.config.ts as `theme.extend`.\n' +
  'module.exports = ' +
  JSON.stringify(themeExtend, null, 2) +
  ';\n';
writeFileSync(OUT_TW, twOut);

console.log(
  `[export-zine-tokens] OK — ${Object.keys(colors).length} colors, ` +
    `${Object.keys(doubles).length} metrics, ${Object.keys(durations).length} durations.\n` +
    `  -> ${OUT_CSS}\n  -> ${OUT_TW}\n` +
    `  spot-checks passed: paper ${colors.paper}, lime ${colors.lime}, coral ${colors.coral}, ` +
    `ink ${colors.ink}, blueInk ${colors.blueInk}.`,
);
